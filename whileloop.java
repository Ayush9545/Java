public class whileloop {
    public static void main(String args[]){
        
        int condition = 0;
        while (condition<10) {
            System.out.println("Hello world");
            condition++;
        }

        System.out.println("Done with Hello World");


        int con1 = 0;
        while (con1<101) {
            System.out.print(con1+" ");
            con1++;
        }
    }
}
