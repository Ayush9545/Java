public class switchcase {
    public static void main (String args[]){
        int number = 1;
        switch (number){
            case 1 : System.out.println("Nah");
            break;

            case 2 : System.out.println("Nah nah");
            break;

            default : System.out.println("Yeahhh");
        }
    }
}
